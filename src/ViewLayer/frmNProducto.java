    public void SeleccionF(){
        int idFarmacia = 0;

     
            for(int i = 0; i< new Farmacia().GetAllModel().getRowCount(); i++){

                if(new Farmacia().GetAllModel().getValueAt(i, 1).equals(cFarmacias.getItemAt(cFarmacias.getSelectedIndex()))){

                    idFarmacia = Integer.parseInt("" + new Farmacia().GetAllModel().getValueAt(i, 0));

                    nombreF = (""+new Farmacia().GetAllModel().getValueAt(i, 1));
                    cFarmacias.setSelectedIndex(idFarmacia);
                    cFarmacias.setSelectedItem(nombreF);
                }
    }
            
            
            
            productos.setIdFarmacia(idFarmacia);
            productos.setNombreF(nombreF);
            
    }
    
    

