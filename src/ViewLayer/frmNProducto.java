    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
      int idFarmacia = 0;

            for(int i = 0; i< new Farmacia().GetAllModel().getRowCount(); i++){

                if(new Farmacia().GetAllModel().getValueAt(i, 1).equals(cFarmacias.getItemAt(cFarmacias.getSelectedIndex()))){

                    idFarmacia = Integer.parseInt("" + new Farmacia().GetAllModel().getValueAt(i, 0));

                    nombreF = (""+new Farmacia().GetAllModel().getValueAt(i, 1));

                }
            }

        if(productos.getIdProducto()> 0){

              
                //productos.setIdFarmacia(idFarmacia);
                productos.setNombreF(nombreF);
                productos.setNombre(txtNombre.getText());
                productos.setCaducidad(txtCaducidad.getText());
                productos.setStock(Integer.parseInt(txtStock.getText()));

                if(productos.Update()){

                  JOptionPane.showMessageDialog(null,"Producto actualizado correctamente, recuerda actualizar desde el boton");

                  this.dispose();

                }

           

        }else{

       

        if(new Producto(0,

                        txtNombre.getText(),

                        txtCaducidad.getText(),

                        Integer.parseInt(txtStock.getText()),

                        idFarmacia,

                        nombreF,

                        1).add()){

            JOptionPane.showMessageDialog(null,"Producto agregado correctamente");

            this.dispose();

        }

       

        }
            
    }//GEN-LAST:event_btnAgregarActionPerformed

